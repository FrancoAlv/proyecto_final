    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int token = 0, dinero = 0, trans = 0, cvv = 0, resultadomas, resultadomenos;

        if (texcodigo.getText().equals("") || texdinero.getText().equals("") || textras.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "PORFAVOR INGRESE TODOS LOS DATOS");
        } else {
            if (texcodigo.getText().matches("[+-]?\\d*(\\.\\d+)?") && texdinero.getText().matches("[+-]?\\d*(\\.\\d+)?") && textras.getText().matches("[+-]?\\d*(\\.\\d+)?") && texcvv.getText().matches("[+-]?\\d*(\\.\\d+)?")) {
                token = Integer.parseInt(texcodigo.getText());
                dinero = Integer.parseInt(texdinero.getText());
                trans = Integer.parseInt(textras.getText());
                cvv = Integer.parseInt(texcvv.getText());
                if (variante == token) {
                    if (agregargente.getLista().get(intro.getNumero()).getPlata() >= dinero && buscarnumerodecuenta(trans) && agregargente.getLista().get(intro.getNumero()).getCvv() == cvv) {
                        resultadomenos = agregargente.getLista().get(intro.getNumero()).getPlata() - dinero;
                        resultadomas = agregargente.getLista().get(numero(trans)).getPlata() + dinero;
                        agregargente.getLista().get(intro.getNumero()).setPlata(resultadomenos);
                        agregargente.getLista().get(numero(trans)).setPlata(resultadomas);
                        dispose();
                    } else {
                        JOptionPane.showMessageDialog(null, "PORFAVOR INGRESE TODOS LOS DATOS CORRETAMENTE ");
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "PORFAVOR INGRESE TODOS LOS DATOS CORRETAMENTE DEL CODIGO DE MOVIENTO BANCARIO");
                }
            } else {
                JOptionPane.showMessageDialog(null, "PORFAVOR INGRESE TODOS LOS DATOS CORRETAMENTE NUMERICOS");
            }

        }
    }//GEN-LAST:event_jButton1ActionPerformed

