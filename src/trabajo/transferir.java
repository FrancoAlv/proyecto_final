    private boolean buscarnumerodecuenta(int trans) {
        for (persona per : agregargente.getLista()) {
            if (per.getNumerodetrans() == trans) {
                return true;
            }
        }
        return false;
    }

    private int numero(int numero2) {
   
        numeroescogido = 0;
        for (persona per : agregargente.getLista()) {
            if (per.getNumerodetrans() == numero2 && agregargente.getLista().get(movientos.getNumero()).getNumerodetrans() !=numero2) {
                return numeroescogido;
            }
            numeroescogido++;
        }
        return numeroescogido;
    }

