    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
   int numero=0,cuenta=0;
       if( !texcuenta.getText().equals("") && !texdinero.getText().equals("") ){ 
   if(texcuenta.getText().matches("[+-]?\\d*(\\.\\d+)?")  && texdinero.getText().matches("[+-]?\\d*(\\.\\d+)?")){
         numero=Integer.parseInt(texdinero.getText());
         cuenta=Integer.parseInt(texcuenta.getText());
            if(agregargente.getLista().get(intro.getNumero()).getNumerodecuenta()==cuenta){
           if(agregargente.getLista().get(intro.getNumero()).getPlata()>numero){
                numero=agregargente.getLista().get(intro.getNumero()).getPlata()-numero;
                agregargente.getLista().get(intro.getNumero()).setPlata(numero);
                dispose();
           }else{
               JOptionPane.showMessageDialog(null,"Saldo insuficiente");
           }
            }else{
                JOptionPane.showMessageDialog(null,"Digite bien el numero de cuenta");
            }
        }else{
             JOptionPane.showMessageDialog(null,"Digite bien los datos");
        }}else{
             JOptionPane.showMessageDialog(null,"LLENE TODAS LAS CASILLAS");
       }
    }//GEN-LAST:event_jButton1ActionPerformed

